#include<stdio.h>

int main()
{
	//Declaration
    int num, square, cube, n;
    num = 1;

	//Data/input
    printf("Enter the value of n : ");
    scanf("%d", &n);

	//Processing/Calculations
    printf("\n-------------------------------------------------\n");
    do
    {
        square = num * num;
        cube = square * num;
        printf("|       %-8d|       %-8d|       %-8d|\n", num, square, cube);
        num++;
    }while(num <= n);

	//Output
    printf("-------------------------------------------------");

	return 0;
}

